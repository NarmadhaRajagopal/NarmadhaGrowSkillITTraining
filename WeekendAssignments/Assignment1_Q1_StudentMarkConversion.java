package WeekendAssignments;

public class Assignment1_Q1_StudentMarkConversion {

	public static void main(String[] args) {
		System.out.println("Problem :Student Mark Conversion\n");
		float mark1 = 87.6F;
		
		System.out.println("OUTPUT\n");
		System.out.println("Float value : " +mark1);
		
		double markconvert1 = mark1;
		System.out.println("Double value : "+markconvert1);
		
		
		int markconvert2 = (int)mark1;
		
		System.out.println("int value : "+markconvert2);
        char charvalue = (char)markconvert2 ;
        System.out.println("character value : "+charvalue);
	}

}
